    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String email=EmailLogin.getText();
        String Password=PasswordLogin.getText();
        String Accountnumber=AccountNoLogin.getText();
        o=Integer.parseInt(Accountnumber);
        /////////MUST MAKE EXCPTION FOR UNCREATED OBJECTS
        try{
            boolean enter= CheckingClass.getlogininputs(email, Password, o);
            if(enter){
                JOptionPane.showMessageDialog(null, "Login Successful");
                CheckingClass.getAccountNumber(o);
                this.dispose();
                new AccountOptions().setVisible(true);

            }
            else{
                JOptionPane.showMessageDialog(null, "Email or password or account number entered isn't correct!");

            }
        }
        catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Account number doesn't exist");
        }
        catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Account number doesn't exist");
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Please enter correct number not text!");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

