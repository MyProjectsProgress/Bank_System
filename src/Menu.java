    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if( StAccName.getText().isEmpty() || StAccPass.getText().isEmpty() ||
            StAccEmail.getText().isEmpty() || StAccIncome.getText().isEmpty()
            || StAccBalance.getText().isEmpty() || StAccPhoneNumb.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter all the fields!");
        }
        else{
            try{
                Random accNum = new Random();
                CheckingClass.i=accNum.nextInt(2000);
                String name=StAccName.getText();
                String Password=StAccPass.getText();
                String Email=StAccEmail.getText();
                String Income= StAccIncome.getText();
                String Balance=StAccBalance.getText();
                String PhoneNumber=StAccPhoneNumb.getText();
                CheckingClass.getinputs(name, Password, Email, Double.parseDouble(Balance),Double.parseDouble(Income),PhoneNumber);

                if(!CheckingClass.checkPhonenumber()){
                    JOptionPane.showMessageDialog(null, "Enter a correct phone number, Starting with Dialing Code");
                }
                else if(!CheckingClass.checkPassword()){
                    JOptionPane.showMessageDialog(null, "The password is too short, At least 8 letters");
                }
                else if(!CheckingClass.checkEmail()){
                    JOptionPane.showMessageDialog(null, "The email you entered isn't correct, Please try again");
                }
                else if(!CheckingClass.checkBalance()){
                    JOptionPane.showMessageDialog(null, "You entered insufficient amount of money, Please try again");
                } 
                else if(!CheckingClass.checkIncome()){
                    JOptionPane.showMessageDialog(null, "You entered insufficient amount of money, Please try again");
                }
                else{

                    JOptionPane.showMessageDialog(null, "Account Succefully created \n"+"Account number= "+CheckingClass.i);
                    dispose();
                    new AccountOptions().setVisible(true);
                    
                }

            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "You entered a text in number area");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

