    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if( NewAccountName.getText().isEmpty() && NewPassword.getText().isEmpty() &&
            NewEmailAddress.getText().isEmpty() && NewPhoneNumber.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter atleast one field!");
        }

        else{
            if(!NewAccountName.getText().isEmpty()){
                String name=NewAccountName.getText();
                CheckingClass.setAccountName(name);
            }
            if(!NewPassword.getText().isEmpty()){
                String password=NewPassword.getText();
                CheckingClass.setPassword(password);
            }
            if(!NewEmailAddress.getText().isEmpty()){
                String EmailAddress=NewEmailAddress.getText();
                CheckingClass.setEmaiLAddress(EmailAddress);
            }
            if(!NewPhoneNumber.getText().isEmpty()){
                String PhoneNumber=NewPhoneNumber.getText();
                CheckingClass.setPhoneNumber(PhoneNumber);
            }
            if(!CheckingClass.checkPassword()){
                JOptionPane.showMessageDialog(null, "The password is too short, Please try again");
            }
            else if(!CheckingClass.checkEmail()){
                JOptionPane.showMessageDialog(null, "The email you entered isn't correct, Please try again");
            }
            else if(!CheckingClass.checkPhonenumber()){
                JOptionPane.showMessageDialog(null, "Enter a correct phone number, Please try again");
            }

            else{
                JOptionPane.showMessageDialog(null, "Account Succesfully Updated");
                
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

