    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(AmountDeposited1.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Enter the missing field");
        }
        else{
            try{
                String AmountDep=AmountDeposited1.getText();
                double AmountDeposited=Double.parseDouble(AmountDep);
                if(CheckingClass.getBalance()<500000 && CheckingClass.getBalance()>0){
                    if(AmountDeposited >=0){
                        if(AmountDeposited+CheckingClass.getCCBalance() <=CheckingClass.getCCLimit()){
                            CheckingClass.DepositCCbalance(AmountDeposited);
                            JOptionPane.showMessageDialog(null, "Amount Deposited in CreditCard: "+AmountDep);
                            
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "You can't exceed the Creditcard limit, Try again");
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Enter correct amount of money");
                    }
                }
                else{
                    if(AmountDeposited >=0){
                        if(AmountDeposited+CheckingClass.getVIPCCBalance() <= CheckingClass.getVIPCCLimit()){
                            CheckingClass.DepositVIPCCbalance(AmountDeposited);
                            JOptionPane.showMessageDialog(null, "Amount Deposited in CreditCard: "+AmountDep);
                            
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "You can't exceed the Creditcard limit, Try again");
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Enter correct amount of money");
                    }
                }
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null,"Please enter correct number not text!");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

